<script>
    document.getElementById("btn_back").onclick = function () {
        window.history.back();
    }

    let n, l;
    const faqs = [false, false, false, false, false];

    function faq(n){
        document.getElementById("faq_" + n).onclick = function (){
            if (faqs[n] === false){
                faq_mininmi();
                faqs[n] = true;
                document.getElementById("art_content_faq_" + n).style.maxHeight = "20rem";
            } else{
                faqs[n] = false;
                document.getElementById("art_content_faq_" + n).style.maxHeight = "0";
            }
        }
    }

    for (n = 0; n <= 4; n++) {
        switch (n){
            case 0: faq(n); break;
            case 1: faq(n); break;
            case 2: faq(n); break;
            case 3: faq(n); break;
            case 4: faq(n); break;
        }
    }

    function faq_mininmi(){
        for (l = 0; l <= 4; l++) {
            if (faqs[l] === true){
                faqs[l] = false;
                document.getElementById("art_content_faq_" + l).style.maxHeight = "0";
            }
        }
    }

</script>